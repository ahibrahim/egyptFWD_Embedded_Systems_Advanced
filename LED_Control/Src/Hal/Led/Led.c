/******************************************************************************
* \Syntax          : Led_Ctrl()                                      
* \Description     : Control Led State
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : LedId: Led ID (R,G,B)                 
* \Parameters (out): State: ON\OFF                                                   
* \Return value:   : None
*******************************************************************************/
void Led_Ctrl (LedType Led, LedStateType State)
{
   switch(Led)
   {
    case LED_RED:
        if(State == LED_ON)
        {
            Dio_WriteChannel(DIO_CHANNEL_F1, DIO_LEVEL_HIGH);
        }
        else
        {
            Dio_WriteChannel(DIO_CHANNEL_F1, DIO_LEVEL_LOW);
        }
    break;
    case LED_BLUE:
        if(State == LED_ON)
        {
            Dio_WriteChannel(DIO_CHANNEL_F2, DIO_LEVEL_HIGH);
        }
        else
        {
            Dio_WriteChannel(DIO_CHANNEL_F2, DIO_LEVEL_LOW);
        }
    break;
    case LED_GREEN:
        if(State == LED_ON)
        {
            Dio_WriteChannel(DIO_CHANNEL_F3, DIO_LEVEL_HIGH);
        }
        else
        {
            Dio_WriteChannel(DIO_CHANNEL_F3, DIO_LEVEL_LOW);
        }
    break;

   }
}

