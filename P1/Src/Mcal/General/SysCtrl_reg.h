#define SYSCTRL_BASEADDR                              0x400FE000
/* SysTick Base address */
#define SYSTICK_BASEADDR                              0xE000E000
/* APINT Base address*/
#define APINT_BASEADDR                                0xE000E000

/* GPIO Clock Register Offsets */
#define RCGCGPIO_OFFSET                               0x608       /* General-Purpose Input/Output Run Mode Clock Gating Control */
/* Status and Ctrl Register Offset */
#define STCTRL_OFFSET                                 0x010
/* Reload Register Offset */
#define STRELOAD_OFFSET                               0x014
/* Current count Register Offset */
#define STCURRENT_OFFSET                              0x018

/* GPIO Clock */
#define RCGCGPIO_REG                                  (*(volatile RCGCGPIO*)(SYSCTRL_BASEADDR + RCGCGPIO_OFFSET))
/* SysTick Registers */
#define STCTRL_REG                                    (*(volatile STCTRL*) (SYSTICK_BASEADDR + STCTRL_OFFSET))
#define STRELOAD_REG                                  (*(volatile STRELOAD*) (SYSTICK_BASEADDR + STRELOAD_OFFSET))
#define STCURRENT_REG                                 (*(volatile STCURRENT*) (SYSTICK_BASEADDR + STCURRENT_OFFSET))



